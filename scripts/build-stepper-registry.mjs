import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.resolve(__dirname, "..");
const checkMode = process.argv.includes("--check");

const sourceFiles = [
  "components/ui/stepper/types.ts",
  "components/ui/stepper/utils.ts",
  "components/ui/stepper/context.tsx",
  "components/ui/stepper/root.tsx",
  "components/ui/stepper/list.tsx",
  "components/ui/stepper/item.tsx",
  "components/ui/stepper/content.tsx",
  "components/ui/stepper/navigation.tsx",
];

const publicValueExports = [
  "Stepper",
  "StepperContent",
  "StepperDescription",
  "StepperIndicator",
  "StepperItem",
  "StepperLabel",
  "StepperList",
  "StepperNext",
  "StepperPrevious",
  "StepperSeparator",
  "StepperTrigger",
  "useStepper",
];

const publicTypeExports = [
  "StepperApi",
  "StepperButtonProps",
  "StepperContentProps",
  "StepperDescriptionProps",
  "StepperIndicatorProps",
  "StepperItemProps",
  "StepperLabelProps",
  "StepperListProps",
  "StepperOrientation",
  "StepperProps",
  "StepperSeparatorProps",
  "StepperStep",
  "StepperStepPosition",
  "StepperStepState",
  "StepperTriggerProps",
];

function stripModuleSyntax(source) {
  return source
    .replace(/^\s*"use client";\s*/gm, "")
    .replace(/^import[\s\S]*?;\r?\n/gm, "")
    .replace(/^export\s+(?:type\s+)?\{[\s\S]*?\};\r?\n?/gm, "")
    .trim();
}

function buildExportBlock(keyword, names) {
  return `${keyword} {\n${names.map((name) => `  ${name},`).join("\n")}\n};`;
}

async function buildStepperSource() {
  const sections = [];

  for (const file of sourceFiles) {
    const source = await readFile(path.join(root, file), "utf8");
    sections.push(`// ${file}\n${stripModuleSyntax(source)}`);
  }

  return `${[
    '"use client";',
    "",
    "// This file is generated by scripts/build-stepper-registry.mjs.",
    "// Edit components/ui/stepper/*, then run pnpm registry:build.",
    "",
    'import * as React from "react";',
    'import { Slot } from "@radix-ui/react-slot";',
    "",
    'import { cn } from "@/lib/utils";',
    "",
    sections.join("\n\n"),
    "",
    buildExportBlock("export", publicValueExports),
    "",
    buildExportBlock("export type", publicTypeExports),
    "",
  ].join("\n")}`;
}

function buildPackageSource(registrySource) {
  return registrySource.replace(
    'import { cn } from "@/lib/utils";',
    [
      'import { clsx, type ClassValue } from "clsx";',
      'import { twMerge } from "tailwind-merge";',
      "",
      "function cn(...inputs: ClassValue[]) {",
      "  return twMerge(clsx(inputs));",
      "}",
    ].join("\n")
  );
}

function buildRegistryItem() {
  return `${JSON.stringify(
    {
      $schema: "https://ui.shadcn.com/schema/registry-item.json",
      name: "stepper",
      type: "registry:ui",
      title: "Stepper",
      description:
        "A lightweight, composable stepper primitive for shadcn/ui-style multi-step flows.",
      dependencies: ["@radix-ui/react-slot"],
      files: [
        {
          path: "registry/default/ui/stepper.tsx",
          type: "registry:ui",
          target: "components/ui/stepper.tsx",
        },
      ],
    },
    null,
    2
  )}\n`;
}

async function writeOrCheck(filePath, content) {
  const absolutePath = path.join(root, filePath);

  if (checkMode) {
    let currentContent;

    try {
      currentContent = await readFile(absolutePath, "utf8");
    } catch {
      throw new Error(`${filePath} is missing. Run pnpm registry:build.`);
    }

    if (currentContent !== content) {
      throw new Error(`${filePath} is out of date. Run pnpm registry:build.`);
    }

    return;
  }

  await mkdir(path.dirname(absolutePath), { recursive: true });
  await writeFile(absolutePath, content);
}

try {
  const registrySource = await buildStepperSource();
  const registryItem = buildRegistryItem();

  await writeOrCheck("registry/default/ui/stepper.tsx", registrySource);
  await writeOrCheck("registry/default/ui/stepper.json", registryItem);
  await writeOrCheck(
    "packages/stepper/src/index.tsx",
    buildPackageSource(registrySource)
  );
  await writeOrCheck(
    "packages/stepper/registry/default/ui/stepper.tsx",
    registrySource
  );
  await writeOrCheck(
    "packages/stepper/registry/default/ui/stepper.json",
    registryItem
  );

  console.log(
    checkMode
      ? "Registry and package output are up to date."
      : "Generated registry and package Stepper artifacts."
  );
} catch (error) {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
}
